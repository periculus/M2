function findFiles(dir, ext, results = []) {
  try {
    const entries = fs.readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory() && !entry.name.startsWith('.') && entry.name !== 'node_modules') {
        findFiles(fullPath, ext, results);
      } else if (entry.isFile() && entry.name.endsWith(ext)) {
        results.push(fullPath);
      }
    }
  } catch (e) { /* skip */ }
  return results;
}

// Extract TripleString regions from the parse tree (not raw indexOf).
// This correctly handles //// escapes inside triple-strings, which the
// naive indexOf("///") approach would misparse as early close delimiters.
function findTripleStringRegions(tree) {
  const regions = [];
  tree.iterate({
    enter: (node) => {
      if (node.name === 'TripleString') {
        regions.push({ from: node.from, to: node.to });
      }
    }
  });
  return regions;
}

function isInRegion(pos, regions) {
  for (const r of regions) {
    if (pos >= r.from && pos < r.to) return true;
    if (r.from > pos) break;
  }
  return false;
}

// Find `document { ... }` blocks via balanced-brace matching.
// Returns sorted array of {from, to} spans.
// CAVEAT: This is a heuristic — it counts raw braces without token awareness,
// so braces inside strings or comments can skew region boundaries. Sufficient
// for error-distribution percentages (doc regions are <1% of errors) but not
// suitable for precise source-mapping.
function findDocumentBlocks(code) {
  const regions = [];
  const re = /\bdocument\s*\{/g;
  let m;
  while ((m = re.exec(code)) !== null) {
    const braceStart = code.indexOf('{', m.index + 8); // skip "document"
    if (braceStart === -1) continue;
    let depth = 1, i = braceStart + 1;
    while (i < code.length && depth > 0) {
      if (code[i] === '{') depth++;
      else if (code[i] === '}') depth--;
      i++;
    }
    if (depth === 0) {
      regions.push({ from: m.index, to: i });
    }
  }
  return regions;
}

function getErrorContext(code, from, to, maxBefore = 15, maxAfter = 15) {
  const effectiveTo = Math.max(to, from + 1);
  const before = code.slice(Math.max(0, from - maxBefore), from)
    .replace(/\n/g, '\\n').replace(/\t/g, '\\t');
  const error = code.slice(from, Math.min(code.length, effectiveTo))
    .replace(/\n/g, '\\n').replace(/\t/g, '\\t');
  const after = code.slice(Math.min(code.length, effectiveTo),
    Math.min(code.length, effectiveTo + maxAfter))
    .replace(/\n/g, '\\n').replace(/\t/g, '\\t');
  return `...${before}>>>${error}<<<${after}...`;
}

