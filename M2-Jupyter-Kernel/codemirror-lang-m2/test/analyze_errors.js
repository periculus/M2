// Scope keywords that take operator arguments
const scopeKeywords = new Set(['symbol', 'global', 'local', 'threadLocal']);

// Operator characters (punctuation that forms M2 operators)
const operatorChars = new Set('+-*/%^~!@#&|<>=.:?_\\'.split(''));

